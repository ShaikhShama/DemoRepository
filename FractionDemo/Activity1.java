package FractionDemo;

public class Activity1 {

	public static void main(String[] args) {
		
		boolean iot = true;
		System.out.println("Boolean:"+iot);
		
		byte range = 11;
		System.out.println("Byte:"+range);
		
		short range1 = -127;
		System.out.println("Short:"+range1);
		
		int range2 = 555;
		System.out.println("Integer:"+range2);
		
		long range3 = 30000000000000L;
		System.out.println("Long:"+range3);
		
		float num1 = 99.99f ;
		System.out.println("Float:"+num1);
		
		double num2 = 899.9889;
		System.out.println("Double:"+num2);
		
		char num3 = 'Z';
		System.out.println("Char:"+num3);
		

	}

}
