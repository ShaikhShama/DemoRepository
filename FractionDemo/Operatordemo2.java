package FractionDemo;

public class Operatordemo2 {

	public static void main(String[] args) {
		
		double percentage = 90.88;
		System.out.println("Assigned Value:" +percentage);
		
		int demo=50;
		demo = demo + 1;
		System.out.println("Value:" +demo);
		
		demo++;  // increment operator
		System.out.println("Value:" +demo);
		
		demo--;  // decrement operator
		System.out.println("Value:" +demo);
		
		++demo; // preincrement
		System.out.println("Value:" +demo);
		
		--demo; // predecrement
		System.out.println("Value:" +demo);
		
		
		int demo1 = 100;
		System.out.println(demo1++);
		System.out.println(demo1);
		
		int example = 30;
		System.out.println(--example);
		
		

	}

}
