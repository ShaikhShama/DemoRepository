package FractionDemo;

public class Operatordemo1 {

	public static void main(String[] args) {
		
		int c = 22;
		int d = 12;
		
		boolean result;
		
		result= (c>d);
		System.out.println("Greater than:" + result);
		
		result= (c<d);
		System.out.println("Less than:" + result);
		
		result= (c>=d);
		System.out.println("Greater than equal:" + result);
		
		result= (c<=d);
		System.out.println("Less than equal:" + result);
		
		result= (c==d);
		System.out.println("Equal to:" + result);
		
		result= (c!=d);
		System.out.println("Not Equal than:" + result);

	}

}
